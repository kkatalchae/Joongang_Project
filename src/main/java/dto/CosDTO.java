package dto;

public class CosDTO {
    private String cosName;
    private String cosLatitude;
    private String cosLongitude;
    private String cosDifficulty;
    private int cosLength;
    private String cosTakeTime;
    private String cosLink;


    public String getCosName() {
        return cosName;
    }

    public void setCosName(String cosName) {
        this.cosName = cosName;
    }

    public String getCosLatitude() {
        return cosLatitude;
    }

    public void setCosLatitude(String cosLatitude) {
        this.cosLatitude = cosLatitude;
    }

    public String getCosLongitude() {
        return cosLongitude;
    }

    public void setCosLongitude(String cosLongitude) {
        this.cosLongitude = cosLongitude;
    }

    public String getCosDifficulty() {
        return cosDifficulty;
    }

    public void setCosDifficulty(String cosDifficulty) {
        this.cosDifficulty = cosDifficulty;
    }

    public int getCosLength() {
        return cosLength;
    }

    public void setCosLength(int cosLength) {
        this.cosLength = cosLength;
    }

    public String getCosTakeTime() {
        return cosTakeTime;
    }

    public void setCosTakeTime(String cosTakeTime) {
        this.cosTakeTime = cosTakeTime;
    }

    public String getCosLink() {
        return cosLink;
    }

    public void setCosLink(String cosLink) {
        this.cosLink = cosLink;
    }



}
